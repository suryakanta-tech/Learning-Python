# Python Assignment Operator Learning Section.

''' Assignment Operators (Python) : Assignment operators are used to store or update values in a variable. '''

# 1. Basic Assignment '='
x = 10
print(x) # output : 10

# 2. Add and Assign '+='
x = 10
x += 5
print(x) # output : 15

# 3. subtract and Assign '-='
x = 10
x -= 5
print(x) # output : 5

# 4. multiply and Assign '-*'
x = 10
x *= 5
print(x) # output : 50

# 5. devide and assign '/='
x = 10
x /= 5
print(x) # output : 2.0 (devide(/) alwaye gives float number.)

# 6. Floor Divide and Assign '//='
x = 10
x //= 5
print(x) # output : 2 (floore division(//) give an integer.)

# 7. Modulus and Assign '%='
x = 10
x %= 5
print(x) # output : 0

# 8. Power and Assign '**='
x = 10
x **= 5
print(x) # output : 100000



